package com.finalkeyword;

public class Demo1 {
public static void main(String[] args) {
	final int i=10;//need to be assigned while declaring.
	System.out.println(i);
	i=i+1;//can't update the value of variable
	System.out.println(++i);//can't update the value of variable
	i=12;//can't update the value of variable.
}
}
